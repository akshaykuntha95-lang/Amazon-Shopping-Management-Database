-- 8️. Find total sales per seller

-- Question: Calculate how much each seller earned in total.

SELECT s.seller_name, SUM(od.subtotal) AS total_sales
FROM SELLER s
JOIN PRODUCT p ON s.seller_id = p.seller_id
JOIN ORDER_DETAILS od ON p.product_id = od.product_id
GROUP BY s.seller_name;


-- Explanation:
-- We join 3 tables and use SUM(od.subtotal) to find total sales per seller.

